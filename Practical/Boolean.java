package SwitchX;

public class Boolean {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 9;
		System.out.println(x > y); // returns true, because 10 is higher than 9
		
		boolean ILoveYou = true;
		boolean IHateYou = false;
		System.out.println("I Love YOU : "+ILoveYou);     // Outputs true
		System.out.println("i Hate You : "+IHateYou);   // Outputs false
	}

}
