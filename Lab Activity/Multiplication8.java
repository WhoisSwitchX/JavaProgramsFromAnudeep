package SwitchX;

public class Multiplication8 {
	public static void main(String args[]) {
		//Declaring the variable
		int Table = 8;

		System.out.println("The Table of 8:  ");
		//Applying for loop
		for(int i=1;i<=10; ++i)
		{  
			//Printing the table of 8 
			System.out.println(Table*i);
		}
	}
}
